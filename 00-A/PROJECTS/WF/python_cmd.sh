

| Command                          | Description                                                      |
|----------------------------------|------------------------------------------------------------------|
| py -m pip install tqdm --no-user | Install the tqdm package using pip without affecting the user    |
| Py -m venv .ven                  | Create a virtual environment in the current directory named .ven |
| pip using pyproject.toml         | Install packages listed in pyproject.toml using pip              |
| pytest --cov=src tests --cov-report=html -v | Run pytest with coverage analysis and generate an html report in verbose mode |
| python -m venv --system-site-packages /path/to/new/virtual/environment | Create a virtual environment based on the system's site-packages directory |